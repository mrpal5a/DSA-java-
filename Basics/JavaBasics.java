public class JavaBasics{
    public static void main(String args[]){
        System.out.println("Hello Anshu");
        // DATA TYPES
        byte b = 8; //byte
        System.out.println(b);

        char ch = 'a'; //char data type
        System.out.println(ch);

        boolean var = true; // boolean type, true/false
        System.out.println(var);

        float price = (float) 10.5; //floating data type
        System.out.println(price);

        int number = 25; //other types for numbers are long and double
        System.out.println(number);

        short n = 240;// short data type not much in use
        System.out.println(n);
    }
}